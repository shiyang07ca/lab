class Solution:
    def cloneGraph(self, node: "Node") -> "Node":
        if not node:
            return node

        used = {}

        def dfs(node):
            if node in used:
                return used[node]

            new = Node(node.val)
            used[node] = new
            for n in node.neighbors:
                new.neighbors.append(dfs(n))
            return new

        return dfs(node)


