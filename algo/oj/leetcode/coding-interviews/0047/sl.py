class Solution:
    def maxValue(self, grid: List[List[int]]) -> int:
        n = len(grid[0])
        f = [0] * (n + 1)
        for row in grid:
            for j, x in enumerate(row):
                f[j + 1] = max(f[j], f[j + 1]) + x
        return f[n]


