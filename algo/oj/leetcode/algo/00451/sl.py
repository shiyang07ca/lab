class Solution:
    def frequencySort(self, s: str) -> str:
        c_count = defaultdict(int)
        for c in s:
            c_count[c] += 1

        pair_list = [(v, c) for c, v in c_count.items()]
        pair_list.sort(reverse=True)

        c_list = []
        for n, c in pair_list:
            for _ in range(n):
                c_list.append(c)

        return "".join(c_list)


