class Trie:
    def __init__(self):
        self.is_leaf = False
        self.next = dict()

    def insert(self, word: str) -> None:
        cur = self
        for c in word:
            if c not in cur.next:
                cur.next[c] = Trie()
            cur = cur.next[c]
        cur.is_leaf = True

    def search(self, word: str) -> bool:
        cur = self
        for c in word:
            if c not in cur.next:
                return False
            cur = cur.next[c]

        return cur.is_leaf

    def startsWith(self, prefix: str) -> bool:
        cur = self
        for c in prefix:
            if c not in cur.next:
                return False
            cur = cur.next[c]

        return True


